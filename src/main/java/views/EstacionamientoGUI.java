    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        var estacionamientoId = txtEstacionamientoId.getText();
        var placa = txtPlaca.getText();
        var fechaRegistro = txtFechaRegistro.getText();
        var disponible = jRadioButton1.isSelected();

        if (estacionamientoId.isEmpty() || placa.isEmpty() || fechaRegistro.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son requeridos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        var fecha = Utils.convertirStringADate(fechaRegistro);

        if (fecha == null) {
            JOptionPane.showMessageDialog(null, "La fecha no tiene el formato correcto", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        var estacionamiento = new Estacionamiento();
        estacionamiento.setEstacionamientoId(Integer.parseInt(estacionamientoId));
        estacionamiento.setPlaca(placa);
        estacionamiento.setFechaRegistro(fecha);
        estacionamiento.setDisponible(disponible);

        try {
            var estacionamientoRepository = new EstacionamientoRepository();

            if (this.isCreate) {
                estacionamientoRepository.save(estacionamiento);
            } else {
                estacionamientoRepository.update(estacionamiento);
            }

            load();
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Ocurrio un error al guardar el estacionamiento", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1MouseClicked

