    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        var estacionamientoId = txtEstacionamientoId.getText();
        if (estacionamientoId.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo de estacionamiento ID es requerido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            var estacionamiento = new Estacionamiento();
            estacionamiento.setEstacionamientoId(Integer.parseInt(estacionamientoId));
            var estacionamientoRepository = new EstacionamientoRepository();
            estacionamientoRepository.delete(estacionamiento);
            JOptionPane.showMessageDialog(null, "Estacionamiento eliminado correctamente", "Exito", JOptionPane.INFORMATION_MESSAGE);
            load();
            clean();
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Ocurrio un error al eliminar el estacionamiento", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2MouseClicked

    private void clean() {
        txtEstacionamientoId.setText("");
        txtPlaca.setText("");
        txtFechaRegistro.setText("");
        jRadioButton1.setSelected(false);
    }
    
    private void load() {
        try {
            var estacionamientoRepository = new EstacionamientoRepository();
            var estacionamientos = estacionamientoRepository.readAll();
            var columnNames = new String[]{"Estacionamiento ID", "Placa", "Fecha Registro", "Disponible"};
            var model = new DefaultTableModel(columnNames, 0);

            for (var estacionamiento : estacionamientos) {
                var row = new Object[]{estacionamiento.getEstacionamientoId(), estacionamiento.getPlaca(), estacionamiento.getFechaRegistro(), estacionamiento.getDisponible()};
                model.addRow(row);
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Ocurrio un error al cargar los estacionamientos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

